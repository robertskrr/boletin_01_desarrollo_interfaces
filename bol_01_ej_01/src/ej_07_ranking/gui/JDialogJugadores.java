    private void validarCampos() {
        jButtonAdd.setEnabled(false);
        ValidationGroup group = validationPanel.getValidationGroup();

        group.add(jTextFieldJugador, StringValidators.REQUIRE_NON_EMPTY_STRING);
        group.add(jTextFieldPuntuacion, StringValidators.REQUIRE_VALID_INTEGER);
        group.add(jTextFieldPuntuacion, StringValidators.REQUIRE_NON_NEGATIVE_NUMBER);
        group.add(jTextFieldNivel, StringValidators.REQUIRE_VALID_INTEGER);
        group.add(jTextFieldNivel, StringValidators.REQUIRE_NON_NEGATIVE_NUMBER);

        validationPanel.addChangeListener((ChangeEvent e) -> {
            if (validationPanel.getProblem() == null) {
                jButtonAdd.setEnabled(true);
            } else {
                jButtonAdd.setEnabled(false);
            }
        });
    }

